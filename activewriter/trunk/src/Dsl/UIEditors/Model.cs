namespace Castle.ActiveWriter
{
	using System;
	using System.Collections.Generic;
	using System.ComponentModel;
	using DslModeling = Microsoft.VisualStudio.Modeling;

    public partial class Model
    {
        List<Import> additionalImportsPropertyStorage = new List<Import>();

        private List<Import> GetAdditionalImportsValue()
        {
            return additionalImportsPropertyStorage;
        }

        private void SetAdditionalImportsValue(List<Import> value)
        {
            additionalImportsPropertyStorage = value;
        }
    }

    [Serializable]
    [DefaultProperty("Name")]
    public class Import
    {
        [CategoryAttribute("Import")]
        [Description("Enter the namespace to be imported in the generated code.")]
        public string Name { get; set; }

        [CategoryAttribute("Import")]
        [Description("Enter the namespace generated by default to be replaced by this import (ex: System.Collections.Generic). This import will replace any previous one with the given name.")]
        public string Replaces { get; set; }
    }
}
